import java.util.Scanner;

public class arithmetic{
   public static void main(String[] args){

      int x=10;
      int y=20;
      int z;
      // addition
      z=x+y;
      System.out.println(z);

      // subtraction
      z=x-y;
      System.out.println(z);

      // multiplication
      z=x*y;
      System.out.println(z);

      // division
      z=y/x;
      System.out.println(z);

      // remainder
      z=x%3;
      System.out.println(z);


      x++;
      System.out.println(x);

      x--;
      System.out.println(x);

   }
}

